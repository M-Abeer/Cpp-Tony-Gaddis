//Example 5.2
#include<iostream>
using namespace std;
int main()
{
	int num=4;
	cout<<num<<endl;//Display 4
	cout<<num++<<endl;//Display 4,then add 1 to number
	cout<<num<<endl;//Display 5
	cout<<++num<<endl;//Display 6
	cout<<"\n";
	
	cout<<num<<endl;
	cout<<num--<<endl;
	cout<<num<<endl;
	cout<<--num<<endl;
	cout<<"\n";
	//Program terminate
	return 0;
}
